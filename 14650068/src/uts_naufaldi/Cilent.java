class ListenFromServer extends Thread {
 
        @Override
        public void run() {
            while (true) {
                try {
                    String msg = (String) input.readObject();
                    String res;
                    String type = msg.split("~")[0];
                    String pengirim = msg.split("~")[1];
                    String text = msg.split("~")[2];
                    String kepada = msg.split("~")[3];
                    
                    switch (type) {
                        case "recieveText":
                            res = pengirim + ": " + text;
                            area_chat.setText(area_chat.getText() + res + "\n");
                            break;
                        case "recievePrivateText":
                            res = pengirim + ": " + text;
                            if (kepada.equals(username)) {
                                area_chat.setText(area_chat.getText() + res + "\n");
                            }
                            break;
                        case "login":
                            area_chat.setText(area_chat.getText() + pengirim + " Sudah Memasuki Chat Grup" + "\n");
                            clientlist.add(pengirim);
                            break;
//                        case "logout":
//                            area_chat.setText(area_chat.getText() + pengirim + " Telah keluar..." + "\n");
//                            clientlist.remove(pengirim);
//                            break;
                        case "list":
                            setTable(text);
                            break;
                    }
                } catch (IOException e) {
                    System.out.println("Server has close the connection: " + e);
                    break;
                } catch (ClassNotFoundException e2) {
                }
            }
        }
 
        private void setTable(String text) {
            int rows = text.split(":").length - 1;
            Object[][] data = new Object[rows][1];
            for (int i = 0; i < rows; i++) {
                String t = text.split(":")[i];
                data[i][0] = t;
            }
 
            String[] header = {"List Member"};
 
            list_user.setModel(new DefaultTableModel(data, header));
        }
    }
