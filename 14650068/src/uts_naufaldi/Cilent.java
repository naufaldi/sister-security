    public boolean start() 
    {
        try {
            socket = new Socket(server, port);
        } catch (Exception ec) {
            System.out.println("Koneksi Error Bro :" + ec);
            return false;
        }
 
        String msg = "Sudah Konek " + socket.getInetAddress() + ":" + socket.getPort();
        System.out.println(msg);
 
        try {
            input = new ObjectInputStream(socket.getInputStream());
            output = new ObjectOutputStream(socket.getOutputStream());
        } catch (IOException eIO) {
            System.out.println("Error di Input dan Output Stream : " + eIO);
            return false;
        }
 
        new Cilent.ListenFromServer().start();
 
        try
        {
            output.writeObject("login~" + username + "~" + username + " telah login ###~server~\n");
            output.writeObject("list~" + username + "~" + username + " telah login ##~server~\n");
 
        } catch (IOException eIO) {
            System.out.println("Salah Login : " + eIO);
//            disconnect();
            return false;
        }
 
        return true;
    }
 
